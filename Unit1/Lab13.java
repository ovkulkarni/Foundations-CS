import edu.fcps.karel2.*;

public class Lab13 extends RandomMap
{
   public static void teleport(){
      if(wallDir<=0.5){
         TeleporterUD.teleportUpDown();
      }
      else{
         TeleporterLR.teleportRightLeft();
      }
   }
   
   public static void main(String[] args)
   {
      Display.setSpeed(10);
      randomMap();
      teleport();
   }  
   

}